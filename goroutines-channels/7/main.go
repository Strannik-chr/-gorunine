package main

func main() {
    ch := make(chan int, 1)
    close(ch)
    ch <- 42 
}

// panic: send on closed channel — возникает, потому что закрытый канал означает
// «больше значений не будет»: отправка в него запрещена, так как получатели
// могут уже завершить работу, считая канал окончательно закрытым.
// Поэтому канал закрывает только отправитель и только после того,
// как он закончил все отправки.